function corFormatoRGB(cor) {
  switch (cor) {
    case "vermelha":
      return "rgb(255,0,0)";
      break;
    case "laranja":
      return "rgb(255,128,0)";
      break;
    case "amarela":
      return "rgb(255,255,0)";
      break;
    case "verde":
      return "rgb(,255,)";
      break;
    case "azul claro":
      return "rgb(0,255,255)";
      break;
    case "azul marinho":
      return "rgb(0,0,255)";
      break;
    case "rosa":
      return "rgb(255,0,255)";
      break;
    default:
      return "rgb(255,255,255)";

  }

}
